# Завдання 6: Нескінченний підрахунок (Тема: Цикли while)
# Сценарій: Програма має зменшувати лічильник на 0.5 на кожній ітерації, поки він не стане рівним нулю.
# Але цикл перетворюється на нескінченний і забиває термінал.

def main():
    countdown = 3.0
    while countdown >= 0.0:
        print(countdown)
        print(countdown)
        countdown -= 0.5  # Зменшуємо крок

main()