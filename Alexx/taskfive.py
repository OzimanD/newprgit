# Черга у магазині
#
# Умова: Є список людей у черзі. Перша людина забирає замовлення і йде.
# Видали першого покупця зі списку та виведи його ім'я на екран, а також покажи, хто залишився.
# Підказка: Метод .pop(0) не просто видаляє елемент за індексом 0, але й повертає його значення.
def shopitems():
    items = ['Олександр', 'Юлія', 'Ірина', 'Олексій']
    print('той хто вийшов з черги: ', items.pop(0))
    print('ті хто лишився в черзі: ', items)

shopitems()